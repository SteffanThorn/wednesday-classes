import mongoose from 'mongoose';

const MONGODB_URI = process.env.MONGODB_URI;

let cached = global.mongoose;

if (!cached) {
  cached = global.mongoose = { conn: null, promise: null };
}

// Check if we should skip DB connection (for build time)
function shouldSkipConnection() {
  // Skip if no URI is provided
  return !MONGODB_URI;
}

async function dbConnect() {
  // Skip connection if no URI is available
  if (shouldSkipConnection()) {
    console.warn('MONGODB_URI not defined - database connection skipped');
    return null;
  }

  if (cached.conn) {
    return cached.conn;
  }

  if (!cached.promise) {
    const opts = {
      bufferCommands: false,
    };

    cached.promise = mongoose.connect(MONGODB_URI, opts).then((mongoose) => {
      return mongoose;
    });
  }

  try {
    cached.conn = await cached.promise;
  } catch (e) {
    cached.promise = null;
    throw e;
  }

  return cached.conn;
}

export default dbConnect;

